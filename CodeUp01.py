# Hello
# 출력하기

print('Hello')

# Hello World
# 출력하기

print('Hello World')

# Hello
# World
# (두 줄에 걸쳐 줄을 바꿔 출력하기)

print('''\
Hello
World
''')

# 'Hello'
# (단, 작은 따옴표도 함께 출력한다.)

print("'Hello'")

# "!@#$%^&*()"
# (단, 큰따옴표도 함께 출력한다.)

print('"!@#$%^&*()"')

# "C:\Download\hello.cpp"
# (단, 큰따옴표도 함께 출력한다.)

print('"C:\Download\hello.cpp"')

